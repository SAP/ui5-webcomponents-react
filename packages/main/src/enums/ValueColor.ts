export enum ValueColor {
  /**
   * Critical value color.
   */
  Critical = 'Critical',
  /**
   * Error value color.
   */
  Error = 'Error',
  /**
   * Good value color.
   */
  Good = 'Good',
  /**
   * Neutral value color.
   */
  Neutral = 'Neutral',
  /**
   * None value color.
   * _Note_: The None value color is set to prevent the display of tooltip 'Neutral' for numeric content.
   */
  None = 'None'
}
